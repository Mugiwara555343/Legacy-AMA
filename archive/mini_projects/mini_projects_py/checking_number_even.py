# Set the initial number to 2
number = 2

# Use a while loop to iterate while the number is less than or equal to 30
while number <= 30:
    # Check if the number is even
    if number % 2 == 0:
        # If it's even, print the number
        print(number)
    
    # Increment the number by 1 for the next iteration
    number += 1


